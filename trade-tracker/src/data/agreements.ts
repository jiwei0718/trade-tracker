export const getAllParties = (): string[] => {
  const partySet = new Set<string>();
  agreements.forEach(a => a.partyNamesZh.forEach(p => partySet.add(p)));
  return Array.from(partySet).sort();
};

export const getEarliestDate = (): string => '1995-01';
export const getLatestDate = (): string => '2026-04';

export const getEffectiveDate = (a: TradeAgreement): string | undefined => {
  const d = a.keyDates;
  return d.in_force ?? d.signed ?? d.concluded ?? d.started ?? d.proposed;
};

export const filterAgreements = (
  all: TradeAgreement[],
  statuses: string[],
  types: string[],
  country: string,
  search: string,
  dateFrom: string,
  dateTo: string,
): TradeAgreement[] => {
  return all.filter(a => {
    if (statuses.length && !statuses.includes(a.status)) return false;
    if (types.length && !types.includes(a.type)) return false;
    if (country && !a.partyNamesZh.some(p => p.includes(country)) && !a.partyNames.some(p => p.toLowerCase().includes(country.toLowerCase()))) return false;
    if (search) {
      const q = search.toLowerCase();
      if (!a.name.toLowerCase().includes(q) && !a.nameZh.includes(q) && !a.descriptionZh.includes(q) && !(a.shortName?.toLowerCase().includes(q))) return false;
    }
    if (dateFrom || dateTo) {
      const d = getEffectiveDate(a);
      if (!d) return false;
      if (dateFrom && d < dateFrom) return false;
      if (dateTo && d > dateTo) return false;
    }
    return true;
  });
};
